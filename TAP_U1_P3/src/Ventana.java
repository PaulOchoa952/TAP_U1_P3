private boolean validaCampo(JTextField t){
        try{
            estaVacio(t);
        }catch(ProyectoException e){
            showMessageDialog(this,e.getMessage()); t.requestFocus();
            return true;
        }
        return false;
    }
    private String CorreoDominio(){
        String a = txt3.getText()+cmb1.getSelectedItem().toString();
        return a;
    }
    
    public String getS(){
        String s="";
        if(masculino.isSelected())s="Masculino";
        if(femenino.isSelected())s="Femenino";
        return s;
    }
    public String getN(){
        String s="";
        if(cbx1.isSelected())s="Si";
        
        
         if(cbx2.isSelected())s="Otra Nacionalidad";
        
        
        return s;
    }
    
    private void estaVacio(JTextField t)throws ProyectoException{
        String cad=t.getText().trim();
        if(cad.equals(""))throw new ProyectoException("Campo vacio");
    }
    
