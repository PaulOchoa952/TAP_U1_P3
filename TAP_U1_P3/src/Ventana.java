    private void btn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn1MouseClicked
        if(validaCampo(txt1))return;
       if(validaCampo(txt2))return;
       if(femenino.isSelected()==false && masculino.isSelected()==false){
            showMessageDialog(this,"SELECCIONE SEXO"); return;
        }
       if(validaCampo(txt3))return;
       if(cbx1.isSelected()==false && cbx2.isSelected()==false){
            showMessageDialog(this,"SELECCIONE Nacionalidad"); return;
        }
       if(cbx1.isSelected()==true && cbx2.isSelected()==true){
            showMessageDialog(this,"SELECCIONE  solo una Nacionalidad"); return;
        }
       
       showMessageDialog(this,"Nombre: "+txt1.getText()+" "+txt2.getText()+"\nSexo:"+getS()+" \nCorreo: "+CorreoDominio()
        +"\nEs Mexicano? : "+ getN());
       
    }//GEN-LAST:event_btn1MouseClicked

